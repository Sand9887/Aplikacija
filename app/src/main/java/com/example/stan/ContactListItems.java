package com.example.stan;

public class ContactListItems {
    String naslov;
    String lokacija;
    String cijena;
    String povrsina;
    String tipProdaje;
    String opis;
    int ID;
    String email;
    String brojMob;

    public String getNaslov() {
        return naslov;
    }

    public void setNaslov(String naslov) {
        this.naslov = naslov;
    }

    public String getLokacija() {
        return lokacija;
    }

    public void setLokacija(String lokacija) {
        this.lokacija = lokacija;
    }

    public String getCijena() {
        return cijena;
    }

    public void setCijena(String cijena) {
        this.cijena = cijena;
    }
    public String getPovrsina() {
        return povrsina;
    }

    public void setPovrsina(String povrsina) {
        this.povrsina = povrsina;
    }

    public String getTipProdaje() {
        return tipProdaje;
    }
    public void setTipProdaje(String tipProdaje) {
        this.tipProdaje = tipProdaje;
    }

    public String getOpis() {
        return opis;
    }
    public void setOpis(String opis) {
        this.opis = opis;
    }

    public int getID() {
        return ID;
    }
    public void setID(int ID) {
        this.ID = ID;
    }

    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public String getBrojMob() {
        return brojMob;
    }
    public void setBrojMob(String brojMob) {
        this.brojMob = brojMob;
    }



}

